class Rektanglene {
  public static void main (String[] args) {
    int lengde;
    int bredde;
    int sum;

    lengde = 3;
    bredde = 5;

    sum = lengde * bredde;
    System.out.println("Sum: " + sum);

    lengde = 7;
    bredde = 3;

    sum = lengde * bredde;
    System.out.println("Sum: " + sum);

    lengde = 4;
    bredde = 9;

    sum = lengde * bredde;
    System.out.println("Sum: " + sum);
  }
}
