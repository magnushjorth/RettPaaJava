class Omkrets {
  public static void main (String[] args) {
    double o;
    double pi;
    double r;
    double d;

    pi = 3.14;
    r = 5;
    d = r*2;

    o = pi * d;
    System.out.println("Omkrets: " + o);

    r = 8;
    d = r*2;

    o = pi * d;
    System.out.println("Omkrets: " + o);

    r = 10;
    d = r*2;

    o = pi * d;
    System.out.println("Omkrets: " + o);
  }
}
